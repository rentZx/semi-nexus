function idOf(term: string) {
  return term.replace(/\s+/g, "-").toLowerCase();
}

export const glossary: GlossaryTerm[] = terms.map(([term, category, plainExplanation]) => ({
  id: idOf(term),
  term,
  category,
  plainExplanation,
  technicalExplanation: `${term}在半导体产业链中通常与设计、制造、封装或应用环节相关，理解它有助于判断公司所处位置和技术壁垒。`,
  relatedSegments: [term, category],
  relatedTerms: terms
    .filter((item) => item[1] === category && item[0] !== term)
    .slice(0, 3)
    .map((item) => item[0]),
  example: `看到“${term}”时，先判断它属于材料、设备、设计、制造、封测还是应用，再看相关公司真正做的是哪一段。`,
}));

export function getTerm(id: string) {
  return glossary.find((term) => term.id === id || term.term === id);
}
